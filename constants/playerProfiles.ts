import { teamRosters } from "./teamRosters";

export type PlayerProfile = {
  name: string;
  chineseName: string;
  position: "门将" | "后卫" | "中场" | "前锋";
  role: string;
  bio: string;
  strengths: string[];
};

export type PlayerProfileMeta = {
  club: string;
  age: number | "待核实";
  caps: number | "待核实";
};

type PlayerProfileSeed = readonly [
  name: string,
  chineseName: string,
  position: PlayerProfile["position"],
  role: string,
  bio: string,
  strengths: string[]
];

type PlayerMetaSeed = readonly [
  name: string,
  club: string,
  age: PlayerProfileMeta["age"],
  caps: PlayerProfileMeta["caps"]
];

const makeProfiles = (players: PlayerProfileSeed[]): Record<string, PlayerProfile> =>
  Object.fromEntries(
    players.map(([name, chineseName, position, role, bio, strengths]) => [
      name,
      { name, chineseName, position, role, bio, strengths }
    ])
  );

const makeMeta = (players: PlayerMetaSeed[]): Record<string, PlayerProfileMeta> =>
  Object.fromEntries(players.map(([name, club, age, caps]) => [name, { club, age, caps }]));

